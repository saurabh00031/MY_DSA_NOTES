#include <bits/stdc++.h>
using namespace std;

int main()
{
  int i,j;
  int H[26]={0};
  char str1[]="decimal";
  char str2[]="medical";
  
  for(i=0;str1[i]!='\0';i++)
  {
    H[str1[i]-97]+=1;
  }
   
  for(i=0;str2[i]!='\0';i++)
  {
    H[str2[i]-97]-=1;
    if(H[str2[i]-97]<0)
    {
      cout<<"Not anagram"<<endl;
      break;
    }
    
  }
  if(str2[i]=='\0')
  {
    cout<<"YES anagram it is \n";
  }
  
    
}


